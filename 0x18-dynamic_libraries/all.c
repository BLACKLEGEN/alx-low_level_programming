#include <stdio.h>

/**
 * add  - add to integers
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}


/**
 * sub  - sub to integers
 * @a: first int
 * @b: second int
 * Return: addition
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul  - mult to integers
 * @a: first int
 * @b: second int
 * Return: multiply
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div  - divide two integers
 * @a: first int
 * @b: second int
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod  - mod of 2 integers
 * @a: first int
 * @b: second int
 * Return: addition
 */
int mod(int a, int b)
{
	return (a % b);
}
